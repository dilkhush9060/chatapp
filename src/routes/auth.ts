import { AsyncErrorHandler } from "@/configs";
import { authController } from "@/controllers";
import { AuthMiddleware, limiter, TokenMiddleware } from "@/middlewares";
import { Router } from "express";

const router = Router();
router.use(limiter(60, 10));

/**
 * @openapi
 * '/auth/signup':
 *   post:
 *     summary: User Sign Up
 *     description: Register a new user with name, email, and password.
 *     tags:
 *       - Auth
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             $ref: '#/components/schemas/SignUpInput'
 *     responses:
 *       200:
 *         description: Sign up successful
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 message:
 *                   type: string
 *                   example: User registered successfully
 *       400:
 *         description: Bad request
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: false
 *                 message:
 *                   type: string
 *                   example: Invalid input data
 *       500:
 *         description: Internal server error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: false
 *                 message:
 *                   type: string
 *                   example: An error occurred during registration
 */
router.post("/signup", AsyncErrorHandler(authController.signUp));

